    public void cargarClientes(){               
        List<CCliente> listaMarcas = cliente_dao.listaClientes();
        tbmMarcas = (DefaultTableModel)tabMarcas.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < listaMarcas.size(); i++) {
            ob[0] = listaMarcas.get(i).getId();
            ob[1] = listaMarcas.get(i).getDni();
            ob[2] = listaMarcas.get(i).getNombres();
            ob[3] = listaMarcas.get(i).getTelefono();
            ob[4] = listaMarcas.get(i).getDireccion();
            ob[5] = listaMarcas.get(i).getEmail();
            tbmMarcas.addRow(ob);
        }        
        tabMarcas.setModel(tbmMarcas);
    }

