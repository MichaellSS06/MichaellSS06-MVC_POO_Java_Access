    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        // TODO add your handling code here:
        String dni = txtDni.getText();
        String nombres = txtNombres.getText();
        String telefono = txtTelefono.getText();
        String direccion = txtDireccion.getText();
        String email = txtEmail.getText();
        // Verificar elementos del formulario
        if ((dni.length()>0) && (nombres.length()>0)&& (telefono.length()>0)&& (direccion.length()>0)&& (email.length()>0)){
            if (cliente_dao.agregarCliente(new CCliente(0, dni, nombres, telefono, direccion, email))){
                JOptionPane.showMessageDialog(this, "Cliente agregado");
                // Limpiar elementos del formulario
                txtDni.setText("");
                txtDni.requestFocus();
                txtNombres.setText("");
                txtTelefono.setText("");
                txtDireccion.setText("");
                txtEmail.setText("");
                for (int i = tbmMarcas.getRowCount()-1; i >=0; i--) {
                    tbmMarcas.removeRow(i);
                }
                cargarClientes();
            }
            else
            JOptionPane.showMessageDialog(this, "Error");
        }
        else
        JOptionPane.showMessageDialog(this, "Los campos no pueden ser vacíos");
    }//GEN-LAST:event_btnGuardarMouseClicked

