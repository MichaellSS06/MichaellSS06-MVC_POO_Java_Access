    private void tabServicioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabServicioMouseClicked
        // TODO add your handling code here:
        Integer k = tabServicio.getSelectedRow();

        txtId.setText(tabServicio.getValueAt(k, 0).toString());
        DateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
        //txtFecha.setText(formatoFecha.format(new Date()));
        txtFecha.setText(formatoFecha.format(tabServicio.getValueAt(k, 1)));
        spTotal.setValue(tabServicio.getValueAt(k, 3));
    }//GEN-LAST:event_tabServicioMouseClicked

    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        // TODO add your handling code here:
        // Dialogo de confirmación
        int opcion = JOptionPane.showConfirmDialog(
            null,
            "¿Está seguro de eliminar venta "+txtId.getText()+"?" ,
            "Eliminar Venta",
            JOptionPane.YES_NO_OPTION);

        if(opcion == JOptionPane.YES_OPTION)
        {
            if (txtId.getText().length()>0){
                int id = Integer.parseInt(txtId.getText());
                if (venta_dao.eliminarVenta(id)){
                    JOptionPane.showMessageDialog(this, "Venta eliminada");
                    txtId.setText("");
                    txtFecha.setText("");
                    for (int i = tbmProductos.getRowCount()-1; i >=0; i--) {
                        tbmProductos.removeRow(i);
                    }
                    cargarVentas();
                }else
                JOptionPane.showMessageDialog(this, "Error");
            }else
            JOptionPane.showMessageDialog(this, "Seleccione una marca");
        }else
        this.dispose();
    }//GEN-LAST:event_btnEliminarMouseClicked

