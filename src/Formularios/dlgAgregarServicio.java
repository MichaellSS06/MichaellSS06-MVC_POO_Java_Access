    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        // TODO add your handling code here:
        String nombre = txtServicio.getText();
        double precio = Double.parseDouble(spPrecio.getValue().toString());
        int stock = Integer.parseInt(spStock.getValue().toString());
        boolean activo = chkboxActivo.isSelected();
        int id_marca = Integer.parseInt(cbxCodigoMarca.getSelectedItem().toString());

        if ((nombre.length()>0) && (precio>0)&& (stock>0)){
            if (servicio_dao.agregarServicio(new CServicio(0, nombre,new CMarca(id_marca,"","",0,0), precio, stock, activo
                ))){
                    JOptionPane.showMessageDialog(this, "Servicio agregado");
                    //
                    txtServicio.setText("");
                    txtServicio.requestFocus();
                    spPrecio.setValue(0);
                    spStock.setValue(0);
                    chkboxActivo.setSelected(false);
                    //
                    cbxMarca.setSelectedIndex(0);
                    cbxCodigoMarca.setSelectedIndex(0);
                    //
                    for (int i = tbmProductos.getRowCount()-1; i >=0; i--) {
                        tbmProductos.removeRow(i);
                    }
                    cargarProductos();
                }
                else
                JOptionPane.showMessageDialog(this, "Error");
        }
        else
        JOptionPane.showMessageDialog(this, "Los campos deben ser v√°lidos");
    }//GEN-LAST:event_btnGuardarMouseClicked

