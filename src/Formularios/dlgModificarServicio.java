    private void btnModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnModificarMouseClicked
        // TODO add your handling code here:
        if (txtId.getText().length()>0){
            Integer id = Integer.parseInt(txtId.getText());
            String nombre = txtServicio.getText();
            double precio = Double.parseDouble(spPrecio.getValue().toString());
            int stock = Integer.parseInt(spStock.getValue().toString());
            boolean activo = chkboxActivo.isSelected();
            int id_marca = Integer.parseInt(cbxCodigoMarca.getSelectedItem().toString());

            if ((nombre.length()>0) && (precio>0)&& (stock>0)){
                if (servicio_dao.modificarServicio(new CServicio(id, nombre,new CMarca(id_marca,"","",0,0), precio, stock, activo
                ))){
                    JOptionPane.showMessageDialog(this, "Servicio modificado");
                    //
                    txtId.setText("");
                    txtServicio.setText("");
                    txtServicio.requestFocus();
                    spPrecio.setValue(0);
                    spStock.setValue(0);
                    chkboxActivo.setSelected(false);
                    //
                    cbxMarca.setSelectedIndex(0);
                    cbxCodigoMarca.setSelectedIndex(0);
                    //
                    for (int i = tbmProductos.getRowCount()-1; i >=0; i--) {
                        tbmProductos.removeRow(i);
                    }
                    cargarProductos();
                }
                else
                JOptionPane.showMessageDialog(this, "Error");
            }
            else
            JOptionPane.showMessageDialog(this, "Los campos deben ser vÃ¡lidos");
        } else
        JOptionPane.showMessageDialog(this, "Seleccione un servicio");
    }//GEN-LAST:event_btnModificarMouseClicked

